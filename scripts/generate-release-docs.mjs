#!/usr/bin/env node
/**
 * generate-release-docs.mjs — Generates RELEASE.md from manifest + GitHub API.
 *
 * Used by the unified-build.yml release job to create rich release notes.
 */
import { readFileSync, writeFileSync } from 'fs';

const TOKEN = process.env.GH_TOKEN || process.env.GITHUB_TOKEN || '';
const TAG = process.env.RELEASE_TAG || 'v1.0.0';
const manifest = JSON.parse(readFileSync('manifest.json', 'utf8'));

async function getLatestCommit(repo) {
  if (!TOKEN) return null;
  try {
    const res = await fetch(
      `https://api.github.com/repos/${repo}/commits/master`,
      { headers: { Authorization: `token ${TOKEN}` } }
    );
    if (!res.ok) return null;
    const data = await res.json();
    return {
      sha: data.sha?.slice(0, 7),
      message: data.commit?.message?.split('\n')[0],
      date: data.commit?.author?.date?.split('T')[0],
    };
  } catch { return null; }
}

async function main() {
  const date = new Date().toISOString().split('T')[0];
  const lines = [];

  lines.push(`# EoS Stack Release — ${TAG}`);
  lines.push(`\n> Released: ${date} | Org: [embeddedos-org](https://github.com/embeddedos-org)`);
  lines.push(`\n---\n`);
  lines.push(`## What is the EoS Stack?\n`);
  lines.push(`The EmbeddedOS Stack is a complete, vertically integrated software and hardware platform.`);
  lines.push(`From bare-metal RTOS firmware to AI agents, desktop IDEs, mobile apps, and web services —`);
  lines.push(`every component is designed to work together and is built from a single manifest.\n`);
  lines.push(`---\n`);
  lines.push(`## Projects in This Release\n`);
  lines.push(`| Tier | Name | Platform | Language | Description |`);
  lines.push(`|------|------|----------|----------|-------------|`);

  for (const p of manifest.projects.filter(p => p.platform !== 'meta')) {
    const commit = await getLatestCommit(p.repo);
    const sha = commit ? ` (\`${commit.sha}\`)` : '';
    lines.push(`| ${p.tier} | [${p.name}](https://github.com/${p.repo})${sha} | ${p.platform} | ${p.language || '—'} | ${p.description.slice(0, 60)} |`);
  }

  lines.push(`\n---\n`);
  lines.push(`## Artifacts\n`);
  lines.push(`All build artifacts are attached to this release:\n`);

  const artifactTypes = {
    'Firmware (ELF/HEX/BIN)': ['eos', 'eBoot', 'eAI', 'eosllm', 'eNI', 'HEALTH-BAND-Neuro'],
    'Desktop (AppImage/DMG/EXE)': ['EoSim', 'EoStudio', 'eVera', 'eBrowser', 'eOffice'],
    'Mobile (APK/IPA)': ['eVera', 'HealthKey-Ulta'],
    'VS Code Extension (VSIX)': ['eVera'],
    'Web (Static)': ['eApps', 'www.embeddedos.org'],
    'Hardware (Gerber/BOM)': ['eCAD-Hardware-Products'],
    'Backend Binary': ['eDB', 'eIPC', 'ebuild'],
  };

  for (const [type, repos] of Object.entries(artifactTypes)) {
    lines.push(`**${type}:** ${repos.map(r => `\`${r}\``).join(', ')}`);
  }

  lines.push(`\n---\n`);
  lines.push(`## Installation\n`);
  lines.push(`### eVera (AI Agent)\n`);
  lines.push('```bash');
  lines.push('curl -fsSL https://raw.githubusercontent.com/embeddedos-org/eVera/master/install.sh | bash');
  lines.push('```\n');
  lines.push(`### Full EoS Stack (from manifest)\n`);
  lines.push('```bash');
  lines.push('git clone https://github.com/embeddedos-org/eos-stack-manifest.git');
  lines.push('cd eos-stack-manifest');
  lines.push('bash scripts/build-all.sh');
  lines.push('```\n');
  lines.push(`### VS Code Extension\n`);
  lines.push('```bash');
  lines.push('code --install-extension evera-ai.vsix');
  lines.push('```\n');
  lines.push(`---\n`);
  lines.push(`## Manifest\n`);
  lines.push(`The full build manifest (\`manifest.json\`) is attached to this release.`);
  lines.push(`It describes every project, its build commands, release targets, and CI configuration.\n`);
  lines.push(`---\n`);
  lines.push(`*Generated by [eos-stack-manifest](https://github.com/embeddedos-org/eos-stack-manifest)*`);

  const content = lines.join('\n');
  writeFileSync('RELEASE.md', content);
  console.log(`✅ RELEASE.md generated for ${TAG}`);
}

main().catch(err => {
  console.error('❌ Release docs generation failed:', err.message);
  process.exit(1);
});
